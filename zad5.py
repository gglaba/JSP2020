
#dzielenie korzystając z // zaokrągla iloraz w dół do najbliższej liczby całkowitej np.
5//2
1//3
#zaokrąglanie za pomocą round(float) pozwala na zaokrąglenie do najbliższej liczby po n miejscach po przecinku,liczbę n możemy określić sami,np.
print(round(10.7))
print(round(10.7,5))
#floor zaokrągla liczbę,zawsze tak aby po zaokrągleniu ta liczba całkowita nie była większa od oryginalnej,np.
